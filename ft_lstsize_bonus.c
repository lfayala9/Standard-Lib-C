#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	size_t	i;
	t_list	*cur;

	i = 0;
	cur = lst;
	while (cur != NULL)
	{
		cur = cur->next;
		i++;
	}
	return (i);
}
